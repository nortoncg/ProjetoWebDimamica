    private void btnExportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportarActionPerformed
        // TODO add your handling code here:
        try {
            Estudante estudantes = new Estudante();
            for (int i = 0; i <= jTable2.getModel().getRowCount() - 1; i++) {
                String monta = "";
                for (int j = 0; j <= jTable2.getModel().getColumnCount() - 1; j++) {
                    monta += (jTable2.getValueAt(i, j).toString() + ";");
                    estudantes.exportarParaTXT(i + 1, monta);
                }
            }
        } catch (Exception ex) {
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_btnExportarActionPerformed

