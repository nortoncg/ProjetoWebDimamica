    private void adicionaNaTabela() {
        // Cria um DefaultTableModel para manipular os dados do JTable. 
        //Os parâmetros são um array que identifica as colunas e um int que seta 
        //o número inicial que linhas
        DefaultTableModel modelo;
        if (jTable2.getModel() == null) {
            modelo = new DefaultTableModel();
            modelo.addColumn("Nome");
            modelo.addColumn("Matriculado");
            modelo.addColumn("Estado");
            modelo.addColumn("Dt. Nasc");
            modelo.addColumn("Senha");
            modelo.addColumn("Obs.:");
        } else {
            modelo = (DefaultTableModel) jTable2.getModel();
        }

        //Monta a arraylist com os valores da tela
        ArrayList<String> valores = new ArrayList<String>();
        valores.add(txtNome.getText());
        if (cbxMatriculado.isSelected()) {
            valores.add("Sim");
        } else {
            valores.add("Não");
        }
        valores.add(cboEstado.getSelectedItem().toString());
        valores.add(txtDataNascimento.getText());
        valores.add(txtSenha.getText());
        valores.add(txtObservacao.getText());

        // Insere os dados no DefaultTableModel
        modelo.addRow(valores.toArray());

        // Associa o DefaultTableModel ao JTable
        jTable2.setModel(modelo);
    }

